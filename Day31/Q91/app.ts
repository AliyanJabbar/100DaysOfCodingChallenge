// Question 91: Create an array of your three favorite fruits and add a new fruit to the end of the array.

// Explain & TIP: Adding elements to an array can be easily done using the .push() method. It allows you to add new items to the end of an array.


let favouriteFruits = ["Mango", "Banana", "Apple"]
favouriteFruits.push("Grapes") //here we add Grapes to the end of an array
console.log(favouriteFruits) //Output= [ 'Mango', 'Banana', 'Apple', 'Grapes' ]